package model;

public class Arrival {
	String air_code;
	String departure_day;
	String departure_time;
	String st_date;
	String ed_date;
	String departure_city;
	public String getAir_code() {
		return air_code;
	}
	public void setAir_code(String air_code) {
		this.air_code = air_code;
	}
	public String getDeparture_day() {
		return departure_day;
	}
	public void setDeparture_day(String departure_day) {
		this.departure_day = departure_day;
	}
	public String getDeparture_time() {
		return departure_time;
	}
	public void setDeparture_time(String departure_time) {
		this.departure_time = departure_time;
	}
	public String getSt_date() {
		return st_date;
	}
	public void setSt_date(String st_date) {
		this.st_date = st_date;
	}
	public String getEd_date() {
		return ed_date;
	}
	public void setEd_date(String ed_date) {
		this.ed_date = ed_date;
	}
	public String getDeparture_city() {
		return departure_city;
	}
	public void setDeparture_city(String departure_city) {
		this.departure_city = departure_city;
	}
	
}
